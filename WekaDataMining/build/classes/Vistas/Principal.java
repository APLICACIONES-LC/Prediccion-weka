    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (!txtruta.getText().equalsIgnoreCase("ruta del archivo .afrr")) {
            jtaresultado.setText("");
            if (jCheckBox2.isSelected()) {
                algo.setCrossValidation(Integer.parseInt(jFormattedTextField1.getText()));
                algo.setTipo(2);
            } else {
                algo.setTipo(1);
            }
            if (zeror.getState()) {
                jtaresultado.setText(algo.ejecutarZeroR());
            } else {
                if (ridor.getState()) {
                    jtaresultado.setText(algo.ejecutarRidor());
                } else {
                    if (part.getState()) {
                        jtaresultado.setText(algo.ejecutarPart());
                    } else {
                        if (nnge.getState()) {
                            jtaresultado.setText(algo.ejecutarNNge());
                        } else {
                            if (M5Rules.getState()) {
                                jtaresultado.setText(algo.ejecutarM5Rules());
                            } else {
                                jtaresultado.setText("");
                                JOptionPane.showMessageDialog(null, "Seleccione un algoritmo de mineria de datos");
                            }
                        }
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un archivo .afrr");
        }

    }//GEN-LAST:event_jButton2ActionPerformed


    JFileChooser fc = new JFileChooser();
    String ruta = null;
    Algoritmos algo;

    public void abrirFileChoser() {
        try {


            jtadatos.setText("");
            fc.setFileFilter(new FiltroFileChoser());
            fc.setCurrentDirectory(new File("src/datos/".replace("/", File.separator)));
            fc.showOpenDialog(this);

            File archivo = fc.getSelectedFile();
            ruta = archivo.getPath().replace("/", File.separator);
            txtruta.setText(ruta);
            algo = new Algoritmos(ruta);
            jtadatos.setText(algo.datos());
        } catch (Exception e) {
        }

    }

